SELECT HALF.FLAVOR
FROM FIRST_HALF HALF
INNER JOIN ICECREAM_INFO INFO
ON HALF.FLAVOR = INFO.FLAVOR
WHERE HALF.TOTAL_ORDER > 3000 AND INFO.FLAVOR = 'fruit_based'
ORDER BY HALF.TOTAL_ORDER DESC

SELECT FLAVOR
FROM FIRST_HALF
WHERE TOTAL_ORDER > 3000 AND FLAVOR = (SELECT FLAVOR FROM ICECREAM_INFO WHERE INGREDIENT_TYPE = 'fruit_based')
ORDER BY TOTAL_ORDER DESC

SELECT *
