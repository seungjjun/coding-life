FROM ANIMAL_OUTS OUTS
RIGHT JOIN ANIMAL_INS INS
ON INS.ANIMAL_ID = OUTS.ANIMAL_ID
WHERE OUTS.DATETIME IS NOT NULL
ORDER BY INS.DATETIME
LIMIT 2

